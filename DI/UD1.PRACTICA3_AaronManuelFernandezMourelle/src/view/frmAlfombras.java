    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            Rug rug = createNewRug();
            lstModel.addElement(rug);
        } catch (RugException ex) {
            errorMessage(ex.getMessage());
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnInfoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInfoActionPerformed
        StringBuilder sb = new StringBuilder();
        List<Rug> selectedValuesList = lstRug.getSelectedValuesList();
        if (selectedValuesList.isEmpty()) {
            sb.append("Nada seleccionado.");
        } else {
            selectedValuesList.forEach((value) -> sb.append(value.fullInfoToString()).append("\n\n"));
        }
        infoMessage(sb.toString());
    }//GEN-LAST:event_btnInfoActionPerformed

    private void lstRugMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lstRugMouseClicked
        if (evt.getClickCount() == 2) {
            Rug selectedValue = lstRug.getSelectedValue();
            if (selectedValue != null) {
                infoMessage(selectedValue.fullInfoToString());
            }
        }
    }//GEN-LAST:event_lstRugMouseClicked

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        int[] selectedIndices = lstRug.getSelectedIndices();
        if (selectedIndices.length == 0) {
            errorMessage("Ninguna alfombra seleccionada.");
            return;
        }
        if (selectedIndices.length > 1) {
            errorMessage("Seleccione solo una alfombra.");
            return;
        }
        lstModel.removeElementAt(selectedIndices[0]);
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void btnRemoveAllActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveAllActionPerformed
        if (lstModel.getSize() > 0) {
            lstModel.removeAllElements();
        }
    }//GEN-LAST:event_btnRemoveAllActionPerformed

