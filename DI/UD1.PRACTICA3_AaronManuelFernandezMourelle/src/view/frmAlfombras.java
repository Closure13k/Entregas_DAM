    private Rug createNewRug() throws RugException {
        RugValidator rugData = RugValidator.validateData(txtName.getText(), txtColor.getText(), txtWidth.getText(), txtHeight.getText());
        if (rugData.getViolations().isEmpty()) {
            return rugData.getRug();
        } else {
            throw new RugException(rugData.buildViolationData());
        }
    }

