    private void btnProfessionRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProfessionRemoveActionPerformed
        int[] selectedProfessions = lstProfession.getSelectedIndices();
        if (selectedProfessions.length < 1) {
            formController.errorMessage(this, "Ninguna profesión seleccionada para su borrado.");
            return;
        }
        for (int i = 0; i < selectedProfessions.length; i++) {
            listModel.removeElement(selectedProfessions[i]);
        }


    }//GEN-LAST:event_btnProfessionRemoveActionPerformed

