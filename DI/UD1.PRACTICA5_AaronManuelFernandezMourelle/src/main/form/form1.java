    private void btnWorkerAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnWorkerAddActionPerformed
        try {
            Worker worker = generateWorker();
            if (tableModel.existsWorker(worker)) {
                formController.errorMessage(this, "El trabajador con DNI " + worker.getId() + " ya está registrado.");
                return;
            }
            tableModel.addWorker(worker);
        } catch (WorkerException ex) {
            formController.errorMessage(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnWorkerAddActionPerformed
    private void btnWorkerRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnWorkerRemoveActionPerformed
        int selectedRow = tblWorker.getSelectedRow();
        if (selectedRow != -1) {
            Worker worker = tableModel.getWorkerAt(selectedRow);
            tableModel.removeRow(selectedRow);
            updateDetails();
            formController.infoMessage(this, "Borrado:\n" + worker.toString());
        }
    }//GEN-LAST:event_btnWorkerRemoveActionPerformed


    private void tblWorkerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblWorkerMouseClicked
        if (evt.getClickCount() == 1) {
            int selectedRow = tblWorker.getSelectedRow();
            if (selectedRow != -1) {
                Worker worker = tableModel.getWorkerAt(selectedRow);
                txtWorkerDetails.setText(worker.toStringFormatted());
            }
        }
        if (evt.getClickCount() == 2) {
            int selectedRow = tblWorker.getSelectedRow();
            if (selectedRow != -1) {
                Worker worker = tableModel.getWorkerAt(selectedRow);
                int countBySimilarProvince = tableModel.countBySimilarProvince(worker);
                formController.infoMessage(this, "Misma provincia: " + countBySimilarProvince + " traballadores.");
            }
        }
    }//GEN-LAST:event_tblWorkerMouseClicked
