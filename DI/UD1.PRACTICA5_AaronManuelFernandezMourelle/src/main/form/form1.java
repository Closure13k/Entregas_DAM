    private Worker generateWorker() throws WorkerException {
        String id = txtID.getText();
        String name = txtName.getText();
        String surnameOne = txtSurnameOne.getText();
        String surnameTwo = txtSurnameTwo.getText();
        String province, profession;
        if (id.isBlank()) {
            throw new WorkerException("El campo DNI " + "no puede estar en blanco.");
        }
        if (name.isBlank()) {
            throw new WorkerException("El campo nombre" + "no puede estar en blanco.");
        }
        if (surnameOne.isBlank()) {
            throw new WorkerException("El campo apellido 1 " + "no puede estar en blanco.");
        }
        if (surnameTwo.isBlank()) {
            throw new WorkerException("El campo apellido 2 " + "no puede estar en blanco.");
        }
        province = (String) cmbProvince.getSelectedItem();
        if (Objects.isNull(province)) {
            throw new WorkerException("El campo provincia no está seleccionado.");
        }
        profession = lstProfession.getSelectedValue();
        if (profession == null) {
            throw new WorkerException("No hay una profesión seleccionada.");
        }

        return new Worker(id, name, surnameOne, surnameTwo, province, profession);
    }

