    private void btnProvinceRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProvinceRemoveActionPerformed
        Object selectedItem = cmbProvince.getSelectedItem();
        if (Objects.isNull(selectedItem)) {
            formController.errorMessage(this, "Ninguna provincia seleccionada.");
            return;
        }
        cmbProvince.removeItem(selectedItem);
        if (cmbProvince.getItemCount() < 1) {
            cmbProvince.setSelectedItem(null);
        }
    }//GEN-LAST:event_btnProvinceRemoveActionPerformed

